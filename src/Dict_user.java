class Dict_user {
    private int id;
    private String surname;
    private String pinyin;

    int getId() {
        return id;
    }

    void setId(int id) {
        this.id = id;
    }

    String getSurname() {
        return surname;
    }

    void setSurname(String surname) {
        this.surname = surname;
    }

    String getPinyin() {
        return pinyin;
    }

    void setPinyin(String pinyin) {
        this.pinyin = pinyin;
    }
}
